package com.example.eshop;



public class categories {


    private int id_category;
    private int name_category;

    private int description_category ;


    public int getId_category() {
        return id_category;
    }

    public void setId_category(int id_category) {
        this.id_category = id_category;
    }

    public int getName_category() {
        return name_category;
    }

    public void setName_category(int name_category) {
        this.name_category = name_category;
    }

    public int getDescription_category() {
        return description_category;
    }

    public void setDescription_category(int description_category) {
        this.description_category = description_category;
    }
}
